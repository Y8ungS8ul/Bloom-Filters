#include <iostream>
#include <stdexcept>

/**
 * @brief Структура узла односвязного списка
 * 
 * Представляет отдельный элемент связного списка,
 * содержащий данные и указатель на следующий узел
 */
struct Nodell 
{
    int data;           ///< Целочисленные данные, хранящиеся в узле
    Nodell* next;       ///< Указатель на следующий узел списка

    /**
     * @brief Конструктор узла
     * @param data Данные для хранения в узле
     */
    Nodell(int data) : data(data), next(nullptr) {}
};

/**
 * @brief Класс односвязного списка (Linked List)
 * 
 * Реализует базовые операции с односвязным списком:
 * - Вставка элемента в конец списка
 * - Проверка существования элемента
 * - Автоматическое освобождение памяти
 * 
 * @note Сложность операций:
 * - Вставка: O(n) - требуется проход до конца списка
 * - Поиск: O(n) - линейный поиск
 * 
 * @warning Не потокобезопасен
 */
class LinkedList 
{
private:
    Nodell* head;       ///< Указатель на первый элемент списка

public:
    /**
     * @brief Конструктор по умолчанию
     * 
     * Создает пустой список с head = nullptr
     */
    LinkedList() : head(nullptr) {}

    /**
     * @brief Вставка элемента в конец списка
     * 
     * @param data Данные для вставки
     * 
     * @details Алгоритм:
     * 1. Создает новый узел с переданными данными
     * 2. Если список пуст, новый узел становится головой
     * 3. Иначе проходит до конца списка и добавляет узел
     * 
     * @note Сложность O(n) из-за необходимости обхода списка
     */
    void insert(int data) {
        Nodell* newNode = new Nodell(data);
        
        if (head == nullptr) 
        {
            // Список пуст - новый узел становится головой
            head = newNode;
        }
        else 
        {
            // Проход до конца списка
            Nodell* current = head;
            while (current->next != nullptr) 
            {
                current = current->next;
            }
            current->next = newNode;
        }
    }

    /**
     * @brief Проверка существования элемента в списке
     * 
     * @param data Искомые данные
     * @return true если элемент найден, false в противном случае
     * 
     * @details Последовательно перебирает все узлы списка,
     *          сравнивая их данные с искомым значением
     * 
     * @note Сложность O(n) в худшем случае
     */
    bool exists(int data) {
        Nodell* current = head;
        while (current != nullptr) {
            if (current->data == data) {
                return true;    // Элемент найден
            }
            current = current->next;
        }
        return false;           // Элемент не найден
    }

    /**
     * @brief Деструктор класса LinkedList
     * 
     * Освобождает всю память, занятую списком.
     * Последовательно удаляет все узлы начиная с головы.
     * 
     * @warning После вызова деструктора список становится невалидным
     */
    ~LinkedList() {
        while (head != nullptr) {
            Nodell* nextNode = head->next;  // Сохраняем указатель на следующий узел
            delete head;                     // Удаляем текущий узел
            head = nextNode;                 // Переходим к следующему
        }
    }
};