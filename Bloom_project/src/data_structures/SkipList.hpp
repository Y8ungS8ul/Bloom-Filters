#ifndef SKIP_LIST_HPP
#define SKIP_LIST_HPP

#include "SkipNode.hpp"
#include <iostream>
#include <cstdlib>
#include <ctime>

/**
 * @brief Список с пропусками (Skip List)
 * 
 * Вероятностная структура данных, позволяющая выполнять операции
 * поиска, вставки и удаления за O(log n) в среднем.
 * 
 * @details Особенности:
 *          - Альтернатива сбалансированным деревьям
 *          - Проще в реализации, чем красно-черные деревья
 *          - Использует случайные уровни для балансировки
 *          - Поддерживает операции поиска, вставки и удаления
 * 
 * Сложность операций в среднем:
 * - Поиск: O(log n)
 * - Вставка: O(log n)
 * - Удаление: O(log n)
 * 
 * @see SkipNode
 */
class SkipList {
private:
    int MAXLVL;     ///< Максимально возможный уровень узла
    float P;        ///< Вероятность повышения уровня (обычно 0.25 или 0.5)
    int level;      ///< Текущий максимальный уровень в списке
    SkipNode* header; ///< Головной узел (фиктивный, не хранит данные)

    /**
     * @brief Генерация случайного уровня для нового узла
     * @return int Случайный уровень от 0 до MAXLVL
     * 
     * @details Использует геометрическое распределение:
     *          Вероятность того, что уровень >= k равна P^k
     */
    int randomLevel();

    /**
     * @brief Создание нового узла
     * @param value Значение для хранения
     * @param level Уровень узла
     * @return SkipNode* Указатель на созданный узел
     */
    SkipNode* createNode(int& value, int level);

public:
    /**
     * @brief Конструктор списка с пропусками
     * @param max_level Максимальный уровень (обычно 16 для 2^16 элементов)
     * @param p Вероятность повышения уровня (0.25 - 0.5)
     * 
     * @details Создает головной узел с фиктивным значением -1
     *          Инициализирует генератор случайных чисел
     */
    SkipList(int max_level, float p);
    
    /**
     * @brief Деструктор списка
     * @details Освобождает память всех узлов списка
     */
    ~SkipList();

    /**
     * @brief Вставка элемента в список
     * @param value Вставляемое значение
     * 
     * @details Алгоритм:
     *          1. Находит позицию для вставки на всех уровнях
     *          2. Генерирует случайный уровень для нового узла
     *          3. Вставляет узел на всех уровнях до сгенерированного
     */
    void insertElement(int& value);
    
    /**
     * @brief Вывод списка на экран
     * @details Отображает все уровни списка с пропусками
     *          Показывает распределение элементов по уровням
     */
    void displayList();
    
    /**
     * @brief Поиск элемента в списке
     * @param value Искомое значение
     * @return true если элемент найден, false в противном случае
     */
    bool search(int value);
    
    /**
     * @brief Удаление элемента из списка
     * @param value Удаляемое значение
     * @return true если элемент был удален, false если не найден
     */
    bool remove(int value);
};

#endif // SKIP_LIST_HPP