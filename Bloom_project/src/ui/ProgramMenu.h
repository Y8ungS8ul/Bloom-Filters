#pragma once
#include <SFML/Graphics.hpp>

/**
 * Пространство имен MainMenu
 * Содержит класс AppMenu для создания текстовых меню в SFML
 */
namespace MainMenu 
{
    /**
     * Класс AppMenu - текстовое меню для SFML приложений
     * 
     * Позволяет создавать вертикальные списки пунктов меню с возможностью
     * навигации по стрелкам (вверх/вниз), выбора пункта по Enter,
     * а также настройки внешнего вида (цвета, шрифт, выравнивание)
     */
    class AppMenu
    {
    private:
        float menu_X;           // Координата X первого пункта меню (по горизонтали)
        float menu_Y;           // Координата Y первого пункта меню (по вертикали)
        int menu_Step;          // Расстояние между пунктами меню по вертикали (в пикселях
        
        int max_menu;           // Максимальное количество пунктов в меню
        int size_font;          // Размер шрифта для текста пунктов меню
        int mainMenuSelected;   // Индекс текущего выбранного пункта меню (начиная с 0)
        
        sf::Font font;          // Объект шрифта (загружается из файла)
        sf::Text* mainMenu;     // Указатель на массив текстовых объектов (пункты меню)
        
        sf::Color menu_text_color = sf::Color::White;   // Цвет обычного (невыделенного) пункта меню
        sf::Color chose_text_color = sf::Color::Yellow; // Цвет выделенного (активного) пункта меню
        sf::Color border_color = sf::Color::Black;      // Цвет обводки текста для всех пунктов
        
        /**
         * Инициализация текстового пункта меню
         * Устанавливает шрифт, цвет, строку, размер, позицию и обводку
         * 
         * @param text - ссылка на объект sf::Text для настройки
         * @param str - текст пункта меню (например, "Играть", "Настройки")
         * @param xpos - координата X расположения пункта
         * @param ypos - координата Y расположения пункта
         */
        void setInitText(sf::Text& text, sf::String str, float xpos, float ypos);
        
        sf::RenderWindow& mywindow;   // Ссылка на графическое окно SFML (для отрисовки)
        
    public:
        // ======================== КОНСТРУКТОР / ДЕСТРУКТОР ========================
        
        /**
         * Конструктор класса AppMenu
         * Создаёт массив текстовых пунктов меню и инициализирует их
         * 
         * @param window - ссылка на графическое окно SFML (куда будет рисоваться меню)
         * @param menux - начальная координата X для первого пункта меню
         * @param menuy - начальная координата Y для первого пункта меню
         * @param index - количество пунктов в меню
         * @param name - массив строк с названиями пунктов меню (широкие строки L"...")
         * @param sizeFont - размер шрифта для пунктов меню (по умолчанию 60)
         * @param step - расстояние между пунктами меню по вертикали (по умолчанию 50)
         */
        AppMenu(sf::RenderWindow& window, float menux, float menuy,
            int index, sf::String name[], int sizeFont = 60, int step = 50);
        
        /**
         * Деструктор класса AppMenu
         * Освобождает память, выделенную под массив текстовых объектов
         */
        ~AppMenu()
        {
            delete[] mainMenu;   // Удаляем массив пунктов меню
        }
        
        // ======================== ОСНОВНЫЕ МЕТОДЫ ========================
        
        /**
         * Отрисовка меню в графическом окне
         * Проходит по всем пунктам меню и отображает их в окне mywindow
         */
        void draw();
        
        /**
         * Перемещение выделения вверх по меню
         * Уменьшает индекс выбранного пункта на 1.
         * При достижении первого пункта переходит на последний (зацикливание)
         */
        void MoveUp();
        
        /**
         * Перемещение выделения вниз по меню
         * Увеличивает индекс выбранного пункта на 1.
         * При достижении последнего пункта переходит на первый (зацикливание)
         */
        void MoveDown();
        
        /**
         * Установка цветовой схемы меню
         * Изменяет цвета всех пунктов меню и их обводки
         * 
         * @param menColor - цвет обычных (невыделенных) пунктов меню
         * @param ChoColor - цвет выделенного (активного) пункта меню
         * @param BordColor - цвет обводки текста для всех пунктов
         */
        void setColorTextMenu(sf::Color menColor, sf::Color ChoColor, sf::Color BordColor);
        
        /**
         * Выравнивание меню по горизонтали
         * Изменяет координату X каждого пункта меню в зависимости от выбранного режима
         * 
         * @param posx - режим выравнивания:
         *               0 - правый край (меню прижато к правой стороне)
         *               1 - левый край (меню прижато к левой стороне)
         *               2 - центр (меню отцентрировано по горизонтали)
         * @param width - ширина окна (нужна для вычисления позиций)
         */
        void AlignMenu(int posx, float width);
        
        /**
         * Получение номера выбранного пункта меню
         * Используется для определения, какой пункт выбрал пользователь
         * 
         * @return int - индекс выбранного пункта (начиная с 0)
         */
        int getSelectedMenuNumber()
        {
            return mainMenuSelected;
        }
    };
}