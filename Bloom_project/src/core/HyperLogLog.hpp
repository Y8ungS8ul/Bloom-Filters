#ifndef HYPERLOGLOG_HPP
#define HYPERLOGLOG_HPP

#include <string>
#include <vector>
#include <cmath>
#include <algorithm>

/**
 * @brief Реализация алгоритма HyperLogLog для приблизительного подсчета уникальных элементов
 * 
 * HyperLogLog - это вероятностная структура данных для оценки количества уникальных
 * элементов в мультимножестве с использованием O(log log n) памяти.
 * 
 * @details Алгоритм использует хеш-функцию и регистры для оценки кардинальности.
 *          Основная идея: подсчет количества ведущих нулей в хеше элемента.
 * 
 * @note Точность оценки зависит от параметра p: чем больше p, тем точнее результат,
 *       но больше потребление памяти (2^p регистров)
 * 
 * @see https://en.wikipedia.org/wiki/HyperLogLog
 */
class HyperLogLog 
{
public:
    /**
     * @brief Конструктор HyperLogLog
     * @param p Параметр точности (количество бит для индексации)
     * 
     * @details Количество регистров m = 2^p
     *          Рекомендуемые значения p от 4 до 16
     *          Память: ~ (2^p) * 6 байт
     */
    explicit HyperLogLog(int p);
    
    /**
     * @brief Вставка элемента в структуру
     * @param element Вставляемый элемент (строка)
     * 
     * @details Алгоритм:
     *          1. Вычисляем хеш элемента
     *          2. Определяем индекс регистра по первым p битам
     *          3. Вычисляем количество ведущих нулей в оставшихся битах (rho)
     *          4. Обновляем регистр, если rho больше текущего значения
     */
    void insert(const std::string& element);
    
    /**
     * @brief Оценка количества уникальных элементов
     * @return double Оценка кардинальности (количества уникальных элементов)
     * 
     * @details Вычисляет гармоническое среднее по регистрам и применяет
     *          корректировку для малых и больших диапазонов
     */
    double estimate() const;
    
    /**
     * @brief Очистка всех регистров (сброс состояния)
     */
    void clear();
    
    /**
     * @brief Получение количества регистров
     * @return size_t Количество регистров (m = 2^p)
     */
    size_t getRegisterCount() const { return m; }

private:
    int p;                      ///< Параметр точности (логарифм количества регистров)
    size_t m;                   ///< Количество регистров (2^p)
    std::vector<int> registers; ///< Массив регистров (хранят максимальные rho)

    /**
     * @brief Вычисление количества ведущих нулей в хеше (rho)
     * @param hash Хеш-значение элемента
     * @return int Количество ведущих нулей + 1
     * 
     * @details Используется для оценки кардинальности:
     *          Чем больше ведущих нулей, тем менее вероятно такое событие
     */
    int getRho(size_t hash) const;
    
    /**
     * @brief Получение корректирующего коэффициента alpha
     * @param p Параметр точности
     * @return double Коэффициент alpha для гармонического среднего
     * 
     * @details Значения взяты из оригинальной статьи HyperLogLog
     */
    double getAlpha(int p) const;
};

#endif // HYPERLOGLOG_HPP