#include <iostream>
#include <vector>
#include <string>
#include <functional> // для std::hash

/**
 * Класс CuckooFilterN - Упрощённая реализация фильтра Кукушки
 * 
 * Реализация вероятностной структуры данных "Фильтр Кукушки" (Cuckoo Filter).
 * Название происходит от поведения кукушки, которая выталкивает яйца из чужих гнёзд.
 * 
 * Особенности данной реализации:
 * - Использует хеш-таблицу с тремя возможными позициями для каждого элемента
 * - При коллизии происходит "вытеснение" - элемент ищет другое место
 * - Поддерживает вставку, удаление и проверку наличия элемента
 * 
 * @note Это упрощённая версия. В классическом фильтре Кукушки используется
 *       несколько хеш-таблиц и более сложный алгоритм вытеснения.
 */
class CuckooFilterN {
private:
    std::vector<std::string> table;  // Хеш-таблица для хранения элементов (строк)
    int size;                        // Размер фильтра (не используется в данной реализации)
    int numBuckets;                  // Количество "корзин" (ячеек) в таблице

public:
    /**
     * Конструктор фильтра Кукушки
     * 
     * @param size - размер фильтра (влияет на количество корзин)
     * 
     * Примечание: В данной реализации количество корзин фиксировано (1000),
     * параметр size используется только для инициализации.
     */
    CuckooFilterN(int size) : size(size), numBuckets(1000) {
        table.resize(numBuckets);     // Создаём таблицу с 1000 пустых ячеек
    }

    /**
     * Вставка элемента в фильтр Кукушки
     * 
     * Алгоритм:
     * 1. Вычисляются три возможных индекса для элемента (хеш-функции)
     * 2. Если хотя бы одна из ячеек пуста, элемент вставляется туда
     * 3. Если все три ячейки заняты, начинается процесс "вытеснения":
     *    - Увеличиваем смещение (i)
     *    - Пересчитываем индексы с учётом смещения
     *    - Пытаемся найти пустую ячейку
     * 4. Если после numBuckets попыток пустая ячейка не найдена, вставка не происходит
     * 
     * @param key - строка для вставки
     * 
     * @note В классическом фильтре Кукушки при коллизии существующий элемент
     *       "вытесняется" на другую позицию, а новый вставляется на его место.
     *       Здесь реализован упрощённый вариант - просто ищется ближайшая пустая ячейка.
     */
    void insert(const std::string& key) 
    {
        int index1 = hash1(key);   // Первая возможная позиция
        int index2 = hash2(key);   // Вторая возможная позиция
        int index3 = hash3(key);   // Третья возможная позиция

        // Проверяем, есть ли свободное место среди трёх основных позиций
        if (table[index1] == "" && table[index2] == "") {
            table[index1] = key;    // Вставляем в первую свободную (index1)
        }
        else {
            // Если все три позиции заняты, начинаем поиск с увеличением смещения
            int i = 1;
            while (i <= numBuckets) {
                // Вычисляем новые индексы с учётом смещения (линейное пробирование)
                index1 = (index1 + i) % numBuckets;
                index2 = (index2 + i) % numBuckets;
                
                // Если нашли пустую ячейку, вставляем элемент
                if (table[index1] == "" && table[index2] == "") {
                    table[index1] = key;
                    break;
                }
                i++;
            }
            // Если свободное место не найдено после numBuckets попыток,
            // элемент не вставляется (можно добавить обработку ошибки)
        }
    }

    /**
     * Проверка наличия элемента в фильтре
     * 
     * Алгоритм:
     * 1. Вычисляются три возможных индекса для элемента
     * 2. Проверяется, равен ли элемент значению в любой из этих ячеек
     * 
     * @param key - проверяемая строка
     * @return true - элемент найден (может быть ложноположительное срабатывание)
     * @return false - элемент точно отсутствует
     * 
     * @note Как и фильтр Блума, возможны ложноположительные срабатывания,
     *       если другой элемент занял все три позиции и дал такую же комбинацию.
     *       Ложноотрицательных срабатываний нет.
     */
    bool contains(const std::string& key) const {
        int index1 = hash1(key);
        int index2 = hash2(key);
        int index3 = hash3(key);

        // Элемент считается присутствующим, если он хранится хотя бы по одному из индексов
        return table[index1] == key || table[index2] == key || table[index3] == key;
    }

    /**
     * Удаление элемента из фильтра
     * 
     * Алгоритм:
     * 1. Вычисляются три возможных индекса для элемента
     * 2. Если элемент найден по какому-либо индексу, ячейка очищается
     * 
     * @param key - строка для удаления
     * 
     * @note В отличие от классического фильтра Блума, фильтр Кукушки
     *       поддерживает операцию удаления без нарушения целостности структуры.
     */
    void remove(const std::string& key) {
        int index1 = hash1(key);
        int index2 = hash2(key);
        int index3 = hash3(key);

        // Проверяем каждую из трёх возможных позиций
        if (table[index1] == key) {
            table[index1] = "";   // Очищаем ячейку
        }
        else if (table[index2] == key) {
            table[index2] = "";
        }
        else if (table[index3] == key) {
            table[index3] = "";
        }
        // Если элемент не найден, ничего не делаем
    }

private:
    /**
     * Первая хеш-функция
     * Вычисляет сумму ASCII-кодов всех символов строки
     * 
     * @param key - входная строка
     * @return int - хеш-значение (индекс в таблице)
     * 
     * @note Простая, но эффективная хеш-функция для строк.
     *       Разные строки могут давать одинаковую сумму (коллизии), но
     *       для фильтра Кукушки это допустимо, так как есть запасные позиции.
     */
    int hash1(const std::string& key) const {
        int hashValue = 0;
        for (char c : key) {
            hashValue += c;   // Суммируем ASCII-коды символов
        }
        return hashValue % numBuckets;
    }

    /**
     * Вторая хеш-функция
     * Вычисляет сумму квадратов ASCII-кодов символов
     * 
     * @param key - входная строка
     * @return int - хеш-значение (индекс в таблице)
     * 
     * @note Отличается от hash1, чтобы обеспечить независимое распределение.
     *       Использование квадратов уменьшает вероятность коллизий для коротких строк.
     */
    int hash2(const std::string& key) const {
        int hashValue = 0;
        for (char c : key) {
            hashValue += c * c;   // Суммируем квадраты ASCII-кодов
        }
        return hashValue % numBuckets;
    }

    /**
     * Третья хеш-функция
     * Использует стандартную библиотечную хеш-функцию std::hash
     * 
     * @param key - входная строка
     * @return int - хеш-значение (индекс в таблице)
     * 
     * @note std::hash обеспечивает хорошее распределение и низкую вероятность коллизий.
     *       Это самая качественная хеш-функция из трёх.
     */
    int hash3(const std::string& key) const {
        return std::hash<std::string>()(key) % numBuckets;
    }
};