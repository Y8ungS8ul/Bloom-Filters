#include <vector>
#include <unordered_map>
#include <functional>
#include <stdexcept>
#include <iostream> 

/**
 * Класс CuckooFilter - Фильтр Кукушки
 * 
 * Реализация вероятностной структуры данных "Фильтр Кукушки" (Cuckoo Filter).
 * Позволяет проверять принадлежность элемента множеству с возможностью удаления.
 * 
 * Отличительные особенности:
 * - Более низкая вероятность ложноположительных срабатываний по сравнению с фильтром Блума
 * - Поддержка операций удаления (в отличие от классического фильтра Блума)
 * - Использует несколько хеш-таблиц и несколько хеш-функций
 * 
 * @tparam T - тип хранимых элементов
 */
template <typename T>
class CuckooFilter {
private:
    // Вектор хеш-таблиц. Каждая таблица хранит элементы и флаги их присутствия
    std::vector<std::unordered_map<T, bool>> tables;
    
    // Вектор хеш-функций, используемых для распределения элементов по таблицам
    std::vector<std::function<size_t(const T&)>> hashFunctions;

public:
    /**
     * Конструктор фильтра Кукушки
     * 
     * @param size - количество хеш-таблиц (по умолчанию 3)
     * 
     * Примечание: Каждая таблица представляет собой unordered_map для быстрого
     * поиска, вставки и удаления элементов (средняя сложность O(1))
     */
    CuckooFilter(size_t size) : tables(3, std::unordered_map<T, bool>()) {
        // Инициализируем три пустые хеш-таблицы
    }

    /**
     * Добавление хеш-функции в фильтр
     * 
     * @param hashFunction - хеш-функция, принимающая элемент типа T и возвращающая size_t
     * 
     * Примечание: Хеш-функции используются для вычисления индексов таблиц,
     * в которые будет помещён элемент. Рекомендуется использовать независимые
     * и равномерно распределяющие хеш-функции (например, SHA256, MurmurHash, MD5)
     */
    void addHashFunction(const std::function<size_t(const T&)>& hashFunction) {
        hashFunctions.push_back(hashFunction);
    }

    /**
     * Вставка элемента в фильтр Кукушки
     * 
     * Алгоритм:
     * 1. Для каждой хеш-функции вычисляется индекс таблицы
     * 2. Пытаемся вставить элемент в одну из таблиц
     * 3. Если все таблицы заняты, запускается процесс "вытеснения" (кукушки)
     * 4. При неудаче после 500 попыток выбрасывается исключение
     * 
     * @param item - элемент для вставки
     * @throws std::runtime_error - если не удалось вставить элемент после 500 попыток
     * 
     * Название "Фильтр Кукушки" происходит от поведения кукушки, которая
     * выталкивает яйца из чужих гнёзд - здесь старые элементы "вытесняются"
     * в другие таблицы, освобождая место для новых
     */
    void insert(const T& item) {
        // Ограничиваем количество попыток для предотвращения бесконечного цикла
        for (size_t attempt = 0; attempt < 500; ++attempt) {
            std::vector<size_t> indices;
            
            // Вычисляем индексы таблиц для данного элемента с помощью всех хеш-функций
            for (const auto& hashFunction : hashFunctions) {
                size_t index = hashFunction(item) % tables.size();
                indices.push_back(index);
            }

            // Пытаемся вставить элемент в одну из таблиц
            if (insertItem(item, indices)) {
                return; // Вставка успешна, выходим из функции
            }
        }
        // Если за 500 попыток не удалось вставить элемент, выбрасываем исключение
        throw std::runtime_error("Не удалось вставить элемент после 500 попыток");
    }

    /**
     * Получение количества хеш-функций
     * 
     * @return size_t - количество зарегистрированных хеш-функций
     */
    size_t getHashFunctionsCount() const {
        return hashFunctions.size();
    }

    /**
     * Вывод содержимого всех таблиц в консоль (для отладки)
     * 
     * Примечание: Метод полезен для визуализации распределения элементов
     * и отладки работы фильтра
     */
    void print() const {
        for (const auto& table : tables) {
            std::cout << "Содержимое таблицы:" << std::endl;
            for (const auto& pair : table) {
                std::cout << "Ключ: " << pair.first << ", Значение: " << pair.second << std::endl;
            }
            std::cout << "-----------------------------" << std::endl;
        }
    }

    /**
     * Проверка наличия элемента в фильтре
     * 
     * Алгоритм:
     * 1. Для каждой хеш-функции вычисляется индекс таблицы
     * 2. Проверяется наличие элемента в каждой из таблиц
     * 3. Если элемент найден во всех таблицах, возвращается true
     * 
     * @param item - проверяемый элемент
     * @return true - элемент возможно присутствует (может быть ложноположительное срабатывание)
     * @return false - элемент точно отсутствует
     * 
     * Примечание: Как и фильтр Блума, возможны ложноположительные срабатывания,
     * но ложноотрицательных срабатываний нет
     */
    bool exists(const T& item) {
        for (const auto& hashFunction : hashFunctions) {
            size_t index = hashFunction(item) % tables.size();
            // Если хотя бы в одной таблице элемента нет - он точно отсутствует
            if (tables[index].find(item) == tables[index].end()) {
                return false;
            }
        }
        return true; // Элемент найден во всех таблицах - возможно присутствует
    }

    /**
     * Удаление элемента из фильтра
     * 
     * Алгоритм:
     * 1. Для каждой хеш-функции вычисляется индекс таблицы
     * 2. Если элемент найден в таблице, он удаляется
     * 3. Возвращается флаг успешности удаления
     * 
     * @param item - элемент для удаления
     * @return true - элемент был удалён (хотя бы из одной таблицы)
     * @return false - элемент не найден ни в одной таблице
     * 
     * Примечание: В отличие от классического фильтра Блума, фильтр Кукушки
     * поддерживает операцию удаления без нарушения целостности структуры
     */
    bool remove(const T& item) {
        bool removed = false;
        for (const auto& hashFunction : hashFunctions) {
            size_t index = hashFunction(item) % tables.size();
            auto it = tables[index].find(item);
            if (it != tables[index].end()) {
                tables[index].erase(it);
                removed = true;
            }
        }
        return removed; // Возвращаем true, если элемент был удалён хотя бы из одной таблицы
    }

private:
    /**
     * Попытка вставки элемента в одну из таблиц
     * 
     * @param item - вставляемый элемент
     * @param indices - вектор индексов таблиц для данного элемента
     * @return true - вставка успешна
     * @return false - не удалось вставить элемент в свободное место
     * 
     * Алгоритм:
     * 1. Сначала пытаемся найти свободное место в любой из таблиц
     * 2. Если свободных мест нет, запускаем процесс вытеснения
     */
    bool insertItem(const T& item, const std::vector<size_t>& indices) {
        // Пытаемся вставить элемент в первую попавшуюся таблицу со свободным местом
        for (size_t i = 0; i < indices.size(); ++i) {
            if (tables[indices[i]].insert({ item, true }).second) {
                return true; // Вставка успешна - нашли свободное место
            }
        }

        // Если свободных мест нет, пытаемся вытеснить существующие элементы
        for (size_t i = 0; i < indices.size(); ++i) {
            if (evictAndInsert(item, indices[i])) {
                return true; // Удалось вытеснить старый элемент и вставить новый
            }
        }
        return false; // Не удалось вставить элемент после всех попыток
    }

    /**
     * Вытеснение элемента и вставка нового (процесс "кукушки")
     * 
     * Алгоритм (рекурсивный):
     * 1. Выбираем случайный элемент из указанной таблицы
     * 2. Удаляем его
     * 3. Пытаемся вставить удалённый элемент в другую таблицу
     * 4. Если вставка не удалась, рекурсивно повторяем процесс
     * 5. Вставляем новый элемент в освободившееся место
     * 
     * @param newItem - новый элемент для вставки
     * @param index - индекс таблицы, в которую пытаемся вставить
     * @return true - вытеснение и вставка успешны
     * @return false - операция не удалась
     * 
     * Примечание: Этот алгоритм дал название фильтру - кукушка вытесняет
     * других птиц из гнезда, так и здесь старые элементы "вылетают",
     * освобождая место для новых
     */
    bool evictAndInsert(const T& newItem, size_t index) {
        // Выбираем первый элемент из таблицы (любой подойдёт для вытеснения)
        auto it = tables[index].begin();
        if (it != tables[index].end()) {
            // Запоминаем старый элемент и удаляем его из таблицы
            T oldItem = it->first;
            tables[index].erase(it);

            // Вычисляем новый индекс для вытесненного элемента (используем следующую хеш-функцию)
            size_t newIndex = hashFunctions[(index + 1) % hashFunctions.size()](oldItem) % tables.size();

            // Рекурсивно пытаемся вставить вытесненный элемент в другую таблицу
            if (!tables[newIndex].insert({ oldItem, true }).second) {
                // Если не удалось вставить, продолжаем вытеснение рекурсивно
                return evictAndInsert(oldItem, newIndex);
            }

            // После успешного вытеснения старого элемента, вставляем новый
            return tables[index].insert({ newItem, true }).second;
        }

        // Если таблица была пуста, просто вставляем новый элемент
        return tables[index].insert({ newItem, true }).second;
    }
};