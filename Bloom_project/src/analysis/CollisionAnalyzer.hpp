#ifndef COLLISION_ANALYZER_HPP
#define COLLISION_ANALYZER_HPP

#include <map>
#include <string>
#include <vector>

/**
 * @file CollisionAnalyzer.hpp
 * @brief Модуль для анализа коллизий в фильтрах Блума
 * 
 * Содержит функции для измерения и анализа коллизий:
 * - Относительные коллизии (на уровне битов)
 * - Абсолютные коллизии (полное совпадение индексов)
 * - Зависимость коллизий от размера фильтра
 */

/**
 * @brief Вычисляет относительное количество коллизий для разных чисел хеш-функций
 * 
 * @param filterSize Размер фильтра Блума (количество битов)
 * @return std::map<double, double> Словарь: [число хеш-функций] -> [количество коллизий]
 * 
 * @details Относительная коллизия возникает, когда несколько элементов
 *          устанавливают один и тот же бит.
 * 
 * Алгоритм:
 * 1. Создает 5 фильтров Блума с разным числом хеш-функций (от 1 до 5)
 * 2. Загружает данные из файла "1000_SalesRecords.csv"
 * 3. Для каждого фильтра вставляет все строки из файла
 * 4. Подсчитывает коллизии с помощью метода countCollisions()
 * 5. Сохраняет результаты в словарь
 * 
 * @note Использует следующие хеш-функции в порядке добавления:
 *       - murmurHash (для 1 ХФ)
 *       - hashFunction1 (для 2 ХФ)
 *       - hashFunction2 (для 3 ХФ)
 *       - hashSHA256 (для 4 ХФ)
 *       - hashMD5 (для 5 ХФ)
 * 
 * @warning Требует наличия файла "1000_SalesRecords.csv" в рабочей директории
 * @see countCollisions()
 */
std::map<double, double> calculateCollisions(size_t filterSize);

/**
 * @brief Вычисляет абсолютное количество коллизий для разных чисел хеш-функций
 * 
 * @param filterSize Размер фильтра Блума (количество битов)
 * @return std::map<double, double> Словарь: [число хеш-функций] -> [количество коллизий]
 * 
 * @details Абсолютная коллизия возникает, когда два разных элемента имеют
 *          полностью совпадающие наборы индексов от всех хеш-функций.
 * 
 * Алгоритм:
 * 1. Создает фильтры с разным числом хеш-функций (от 1 до 5)
 * 2. Загружает данные из файла "100_SalesRecords.csv"
 * 3. Вставляет строки в фильтр
 * 4. Подсчитывает абсолютные коллизии с помощью retFullCollisions()
 * 5. Сохраняет результаты в словарь
 * 
 * @note В отличие от calculateCollisions(), этот метод:
 *       - Использует меньший файл (100 строк вместо 1000)
 *       - Добавляет хеш-функции каскадно (накапливает их)
 *       - Подсчитывает полные совпадения индексов
 * 
 * @warning Сложность O(n²) из-за retFullCollisions(), используйте только для анализа
 * @see retFullCollisions()
 */
std::map<double, double> calculateCollisionsABS(size_t filterSize);

/**
 * @brief Анализирует зависимость коллизий от размера фильтра при фиксированном числе хеш-функций
 * 
 * @param hashfunctions_count Количество хеш-функций (обычно 3)
 * @return std::map<int, int> Словарь: [размер фильтра] -> [количество коллизий]
 * 
 * @details Исследует, как увеличение размера фильтра влияет на количество коллизий.
 * 
 * Алгоритм:
 * 1. Перебирает размеры фильтра от 1000 до 30000 с шагом 5000
 * 2. Для каждого размера создает фильтр с hashfunctions_count хеш-функциями
 * 3. Использует фиксированный набор хеш-функций:
 *    - hashFunction1
 *    - hashFunction2
 *    - murmurHash
 * 4. Загружает данные из файла "1000_SalesRecords.csv"
 * 5. Подсчитывает коллизии с помощью countCollisions()
 * 6. Сохраняет результаты в словарь
 * 
 * @note Позволяет оценить оптимальный размер фильтра для минимизации коллизий
 * @warning Размеры фильтра должны быть в разумных пределах (1000-30000)
 * 
 * @example
 * auto result = calculateCollisions_size(3);
 * // Результат: {1000 -> коллизии, 6000 -> коллизии, ..., 26000 -> коллизии}
 */
std::map<int, int> calculateCollisions_size(int hashfunctions_count);

#endif // COLLISION_ANALYZER_HPP